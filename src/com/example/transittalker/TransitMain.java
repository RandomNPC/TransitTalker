	//select route
	public void selectRoute(String rC){
		routeFocus = null;
		for(struct l: route){ 
			if(isEqual(rC,l.routeCode())){
				routeFocus = l;
				break;
			}
		}
	}
	
	
	public void selectRoute(String rC, String pR){
		
		routeFocus = null;
		for(struct l: route){ 
			if(isEqual(rC,l.routeCode()) && isEqual(pR,l.prCode())){
				routeFocus = l;
			}
		}
	}
	
